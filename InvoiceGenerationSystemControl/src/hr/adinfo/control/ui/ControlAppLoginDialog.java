    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
		String pass = String.valueOf(jPasswordFieldPassword.getPassword());
		
		if("".equals(pass)){
			ControlAppLogger.GetInstance().ShowMessage("Unesite lozinku");
			return;
		}
		
		if(jComboBoxUsers.getSelectedIndex() < 0 || jComboBoxUsers.getSelectedIndex() >= 2){
			ControlAppLogger.GetInstance().ShowMessage("Odaberite korisnika");
			return;
		}

		if(jComboBoxUsers.getSelectedIndex() == 0 && "user123".equals(pass)){
			loginSuccess = true;
			ControlApp.controlAppUserType = 0;
			Utils.DisposeDialog(this);
		} else if(jComboBoxUsers.getSelectedIndex() == 1 && "98imunzb7".equals(pass)){
			loginSuccess = true;
			ControlApp.controlAppUserType = 1;
			Utils.DisposeDialog(this);
		} else {
			ControlAppLogger.GetInstance().ShowMessage("Pogrešna lozinka.");
		}
		
    }//GEN-LAST:event_jButtonLoginActionPerformed

