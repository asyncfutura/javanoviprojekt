	public ControlAppLoginDialog(java.awt.Frame parent, boolean modal) {
		super(parent, modal);
		initComponents();
		
		final Window thisWindow = this;
		KeyboardFocusManager.getCurrentKeyboardFocusManager().addKeyEventDispatcher(new KeyEventDispatcher() {
			@Override
			public boolean dispatchKeyEvent(KeyEvent ke) {
				if(!thisWindow.isDisplayable()){
					KeyboardFocusManager.getCurrentKeyboardFocusManager().removeKeyEventDispatcher(this);
				}
				
				if(!thisWindow.isFocused())
					return false;
				
				if(ke.getID() == KeyEvent.KEY_PRESSED){
					if(ke.getKeyCode() == KeyEvent.VK_ESCAPE){
						jButtonExitActionPerformed(null);
					} else if(ke.getKeyCode() == KeyEvent.VK_F8){
						jButtonLoginActionPerformed(null);
					} else if(ke.getKeyCode() == KeyEvent.VK_ENTER){
						jButtonLoginActionPerformed(null);
					} else if(ke.getKeyCode() == KeyEvent.VK_F6){
						// TODO remove used for testing
						jComboBoxUsers.setSelectedIndex(1);
						jPasswordFieldPassword.setText("98imunzb7");
						jButtonLogin.doClick();
					} else if(ke.getKeyCode() == KeyEvent.VK_F7){
						// TODO remove used for testing
						jComboBoxUsers.setSelectedIndex(0);
						jPasswordFieldPassword.setText("user123");
						jButtonLogin.doClick();
					}
				}
				
				return false;
			}
		});

		// Default values setup
		DefaultComboBoxModel defaultComboBoxModel = new DefaultComboBoxModel();
		defaultComboBoxModel.addElement("Izaberite");
		jComboBoxUsers.setModel(defaultComboBoxModel);
		
		SetupUsersComboBox();
	}
	
